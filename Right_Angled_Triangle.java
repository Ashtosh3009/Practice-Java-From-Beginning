//W.A.P. to print the area of Right-angled Triangle having base 10 and height=5.

public class Right_Angled_Triangle {
    public static void main(String args[])
    {
        int b=10,h=5;
        double a;
        a=0.5*h*b;
        System.out.println("Area of Right-angled Triangle = "+a);
    }
}

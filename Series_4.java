// W.A.P. to print following series
// 0 7 26 ...... 999
import java.util.*;
public class Series_4 {
    public static void main(String args[])
    {
        int i;
        for(i=1;i<=10;i++)
        {
            System.out.print((i*i*i)-1+" ");
        }
        System.out.println();
    }
}
